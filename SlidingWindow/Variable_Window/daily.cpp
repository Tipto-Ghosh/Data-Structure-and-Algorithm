class Solution {
public:
	
    int robotSim(vector<int>& arr , vector<vector<int>>& obs) {
        vector<vector<int>> direction = {{0,1} , {1,0} , {0,-1} , {-1,0}};
        int x = 0 , y = 0 , d = 0;
        int ans = 0;
        int n = arr.size() , n2 = obs.size();
        set<pair<int , int>> Set;
        for(int i = 0; i < n2; i++) {
          Set.insert(make_pair(obs[i][0] , obs[i][1]));
        }

        for(int i = 0; i < n; i++) {
        	
            if(arr[i] == -1) {
                d = (d+1) % 4;
            }
            else if(arr[i] == -2) {
                d = (d+3) % 4;
            }
        	else { // need to move
               int dx = direction[d][0];
               int dy = direction[d][1];
               int k = arr[i];
               for(int j = 1; j <= k; j++) {
               	   int nx = x + dx;
               	   int ny = y + dy;
               	   if(Set.find(make_pair(nx , ny)) != Set.end()) {
               	   	  break;
               	   }
               	   else {
               	   	  x = nx;
               	   	  y = ny;
               	   }
               	   ans = max(ans , x*x + y*y);
               }
        	}
        }   

      return ans;    
    }
};




